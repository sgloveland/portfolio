import {CSSProperties, ReactElement, useState, useEffect} from 'react'
import type { NextPage } from 'next'
import Head from 'next/head'
import styles from '../styles/Home.module.css'
import {useTransition, animated, AnimatedProps, useSpringRef} from '@react-spring/web'

import Experience from './experience'
import Education from './education'
import Skills from './skills'
import Project from './project'
import BioCard from '../components/BioCard'
import { NavBar } from '../components/NavBar'

const pages: ((props: AnimatedProps<{style: CSSProperties}>) => ReactElement)[] = [
  ({style}) => <animated.div style={{...style}}><BioCard /></animated.div>,
  ({style}) => <animated.div style={{...style}}><Skills /></animated.div>,
  ({style}) => <animated.div style={{...style}}><Experience /></animated.div>,
  ({style}) => <animated.div style={{...style}}><Project /></animated.div>,
  ({style}) => <animated.div style={{...style}}><Education /></animated.div>,
]

const Home: NextPage = () => {
  const [activePageIndex, setActivePageIndex] = useState(0)
  const transRef = useSpringRef()
  const transitions = useTransition(activePageIndex, {
    ref: transRef,
    exitBeforeEnter: true,
    keys: null,
    from: { opacity: 0, transform: 'translate3d(100%,0,0)' },
    enter: { opacity: 1, transform: 'translate3d(0%,0,0)' },
    leave: { opacity: 0, transform: 'translate3d(-50%,0,0)' },
  })

  useEffect(() => {
    transRef.start()
  }, [activePageIndex])

  return (
    // onClick={() => setActivePageIndex(state => (state + 1) % pages.length)}
    <div className={styles.container} >
      <Head>
        <title>Sean Loveland</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="profile.jpg" />
      </Head>
      <NavBar onChange={(index: number) => {setActivePageIndex(index)}}/>

      {transitions((style, i) => {
        const Page = pages[i]
        return <Page style={style}/>
      })}
    </div>
  )
}

export default Home
